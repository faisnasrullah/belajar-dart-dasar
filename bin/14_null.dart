/// dokumentasi belajar null di dart
void main() {
  /**
   * null == data kosong
   * digunakan jika kita ingin membuat sebuah variabel yg bisa
   * diisi dengan data null, kita bisa tambahkan karakter ?
   * ex :
   * TipeData? namaVariabel = null;
   * 
   */

  int number1;
  int? number2;

  print(number1);
  print(number2);
}
