/// dokumentasi belajar while loop di dart
void main() {
  /**
   * while loop adalah versi perulangan yg lebih sederhana
   * hanya terdapat kondisi perulangan tanpa ada init dan post state
   */

  var counter = 1;
  while (counter <= 10) {
    print('Perulangan ke-$counter');
    counter++;
  }
}
