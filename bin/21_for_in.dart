/// dokumentasi belajar for in di dart
void main() {
  /**
   * mengakses data list menggunakan perulangan sangat bertele-tele
   * cara yg lebih cepat adalah menggunakan for in
   */

  var array = <String>['Eko', 'Fais', 'Farid', 'Ronal', 'Arif'];

  for (var value in array) {
    print(value);
  }
}
