/// dokumentasi belajar operator perbandingan di dart
void main() {
  /**
   * '==' -> Sama dengan
   * '!=' -> Tidak sama dengan
   * '>' -> Lebih dari
   * '<' -> Kurang dari
   * '>=' -> Lebih dari atau sama dengan
   * '<=' -> Kurang dari atau sama dengan
   */

  print(10 == 10);
  print(10 != 10);
  print(10 > 10);
  print(10 < 10);
  print(10 >= 10);
  print(10 <= 10);
  print('fais' == 'nasrullah');
}
